package com.model;

public class Item {
	String itemId;
	String itemName;
	String itemType;
	String itemRefNumber;
	String brand;
	String openingStock;
	String currentStock;
	String rejectedStock;
	String reserveStock;
	String minimumOrderQty;
	String rate;
	String uom;
	String itemRemarks;
	String inventoryCategory;
	public String getItemId() {
		return itemId;
	}
	public void setItemId(String itemId) {
		this.itemId = itemId;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemType() {
		return itemType;
	}
	public void setItemType(String itemType) {
		this.itemType = itemType;
	}
	public String getItemRefNumber() {
		return itemRefNumber;
	}
	public void setItemRefNumber(String itemRefNumber) {
		this.itemRefNumber = itemRefNumber;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getOpeningStock() {
		return openingStock;
	}
	public void setOpeningStock(String openingStock) {
		this.openingStock = openingStock;
	}
	public String getCurrentStock() {
		return currentStock;
	}
	public void setCurrentStock(String currentStock) {
		this.currentStock = currentStock;
	}
	public String getRejectedStock() {
		return rejectedStock;
	}
	public void setRejectedStock(String rejectedStock) {
		this.rejectedStock = rejectedStock;
	}
	public String getReserveStock() {
		return reserveStock;
	}
	public void setReserveStock(String reserveStock) {
		this.reserveStock = reserveStock;
	}
	public String getMinimumOrderQty() {
		return minimumOrderQty;
	}
	public void setMinimumOrderQty(String minimumOrderQty) {
		this.minimumOrderQty = minimumOrderQty;
	}
	public String getRate() {
		return rate;
	}
	public void setRate(String rate) {
		this.rate = rate;
	}
	public String getUom() {
		return uom;
	}
	public void setUom(String uom) {
		this.uom = uom;
	}
	
	public String getItemRemarks() {
		return itemRemarks;
	}
	public void setItemRemarks(String itemRemarks) {
		this.itemRemarks = itemRemarks;
	}
	public String getInventoryCategory() {
		return inventoryCategory;
	}
	public void setInventoryCategory(String inventoryCategory) {
		this.inventoryCategory = inventoryCategory;
	}

}
