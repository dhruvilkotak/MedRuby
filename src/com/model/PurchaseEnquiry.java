package com.model;

public class PurchaseEnquiry {
String enquiryNumber;
String enquiryDate;
String mrvNumber;
String priority;
String followupBy;
public String getEnquiryNumber() {
	return enquiryNumber;
}
public void setEnquiryNumber(String enquiryNumber) {
	this.enquiryNumber = enquiryNumber;
}
public String getEnquiryDate() {
	return enquiryDate;
}
public void setEnquiryDate(String enquiryDate) {
	this.enquiryDate = enquiryDate;
}
public String getMrvNumber() {
	return mrvNumber;
}
public void setMrvNumber(String mrvNumber) {
	this.mrvNumber = mrvNumber;
}

public String getPriority() {
	return priority;
}
public void setPriority(String priority) {
	this.priority = priority;
}
public String getFollowupBy() {
	return followupBy;
}
public void setFollowupBy(String followupBy) {
	this.followupBy = followupBy;
}

}
