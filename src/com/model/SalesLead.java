package com.model;

public class SalesLead {
	
	String leadNumber;
	String leadName;
	String leadDate;
	String customerId;
	String leadRequirement;
	String leadSource;
	String estimatedValue;
	String nextFollowUpDate;
	String generatedBy;
	String sector;
	String preparedBy;
	String remarks;
	public String getLeadNumber() {
		return leadNumber;
	}
	public void setLeadNumber(String leadNumber) {
		this.leadNumber = leadNumber;
	}
	public String getLeadName() {
		return leadName;
	}
	public String getPreparedBy() {
		return preparedBy;
	}
	public void setPreparedBy(String preparedBy) {
		this.preparedBy = preparedBy;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public void setLeadName(String leadName) {
		this.leadName = leadName;
	}
	public String getLeadDate() {
		return leadDate;
	}
	public void setLeadDate(String leadDate) {
		this.leadDate = leadDate;
	}
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public String getLeadRequirement() {
		return leadRequirement;
	}
	public void setLeadRequirement(String leadRequirement) {
		this.leadRequirement = leadRequirement;
	}
	public String getLeadSource() {
		return leadSource;
	}
	public void setLeadSource(String leadSource) {
		this.leadSource = leadSource;
	}
	public String getEstimatedValue() {
		return estimatedValue;
	}
	public void setEstimatedValue(String estimatedValue) {
		this.estimatedValue = estimatedValue;
	}
	public String getNextFollowUpDate() {
		return nextFollowUpDate;
	}
	public void setNextFollowUpDate(String nextFollowUpDate) {
		this.nextFollowUpDate = nextFollowUpDate;
	}
	public String getGeneratedBy() {
		return generatedBy;
	}
	public void setGeneratedBy(String generatedBy) {
		this.generatedBy = generatedBy;
	}
	public String getSector() {
		return sector;
	}
	public void setSector(String sector) {
		this.sector = sector;
	}

	
}
